package uchuca.domain;

public class Reaccion {

    private Integer id;
    private Integer totalReaccion;
    private Integer idReaccion;
    private Integer idProyecto;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getTotalReaccion() {
        return totalReaccion;
    }

    public void setTotalReaccion(Integer totalReaccion) {
        this.totalReaccion = totalReaccion;
    }

    public Integer getIdReaccion() {
        return idReaccion;
    }

    public void setIdReaccion(Integer idReaccion) {
        this.idReaccion = idReaccion;
    }

    public Integer getIdProyecto() {
        return idProyecto;
    }

    public void setIdProyecto(Integer idProyecto) {
        this.idProyecto = idProyecto;
    }
}
